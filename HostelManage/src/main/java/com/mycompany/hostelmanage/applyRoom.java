    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        if (hasAc.isSelected()) {
            ac_Nac = "AC";
        } else if (hasNonac.isSelected()) {
            ac_Nac = "NON AC";
        }
        if (DS.isSelected()) {
            shari = "Double Sharing";
        } else if (TS.isSelected()) {
            shari = "Triple Sharing";
        }
        String not = "not";
        String ids = LoginPage.login_id_user.getText();
        String registerUserQuery = "update  register set sharing='" + shari + "',Room_Ac_NonAc='" + ac_Nac + "' ,Room_allocated='" + not + "' where emailid='" + ids + "'";

        try {

            pst = registration.db.mycon().prepareStatement(registerUserQuery);

            if (pst.executeUpdate() != 0 ) {
                 
                JOptionPane.showMessageDialog(null, "You have choosen " + ac_Nac + " and " + shari + " Room");

                int result = JOptionPane.showConfirmDialog(null, "Do You want to continue?", "Confirmation",
                        JOptionPane.YES_NO_OPTION,
                        JOptionPane.QUESTION_MESSAGE);
                if (result == JOptionPane.YES_OPTION) {
                    
                    new payFees().setVisible(true);
                } else if (result == JOptionPane.NO_OPTION) {
                    new student_home().setVisible(true);
                }
            } else {

                JOptionPane.showMessageDialog(null, "Read All terms & conditions");
                
                
            }

        } catch (Exception e) {
            System.out.println(e);
        } finally {
            this.dispose();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

