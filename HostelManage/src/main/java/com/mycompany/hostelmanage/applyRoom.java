    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String ids = LoginPage.login_id_user.getText();
        String fee = "Paid";
        String Not = "NOT ON LEAVE";
        int amt = 0;

        if (applyRoom.ac_Nac == "AC" && applyRoom.shari == "Double Sharing") {
            amt = 50000;
        } else if (applyRoom.ac_Nac == "AC" && applyRoom.shari == "Triple Sharing") {
            amt = 40000;
        } else if (applyRoom.ac_Nac == "NON AC" && applyRoom.shari == "Double Sharing") {
            amt = 35000;
        } else {
            amt = 25000;
        }

        String registerUserQuery = "update  register set Fee_details='" + fee + "',total_feePaid_RS='" + amt + "',Leav_not='" + Not + "' where emailid='" + ids + "'";

        try {

            pst = registration.db.mycon().prepareStatement(registerUserQuery);

            if (pst.executeUpdate() != 0) {
                txt.setText(String.valueOf(amt) + " Rupees per Month");
            } else {

                JOptionPane.showMessageDialog(null, "Try Again Later"
                        + "Sorry for inconvenince");
            }

        } catch (Exception e) {
            System.out.println(e);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

